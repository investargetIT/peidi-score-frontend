<template>
  <div class="mt-3 rounded-sm">
    <el-table
      :data="tableData"
      style="width: 100%"
      :empty-text="t('table.emptyText')"
    >
      <el-table-column prop="createAt" :label="t('history.date')">
      </el-table-column>
      <el-table-column
        prop="productName"
        :label="t('history.description')"
      ></el-table-column>

      <el-table-column prop="statusName" :label="t('history.type')">
        <template #default="scope">
          <div class="flex gap-2">
            <el-popover
              v-for="(status, index) in getStatusTags(scope.row.statusName)"
              :key="index"
              placement="top"
              trigger="hover"
              :content="`记录数：${status?.number}`"
            >
              <template #reference>
                <el-tag
                  class="mx-1"
                  :type="status?.text === '审核通过' ? 'success' : 'info'"
                >
                  {{ status?.text }}
                </el-tag>
              </template>
            </el-popover>
          </div>
        </template>
      </el-table-column>
      <el-table-column
        prop="productName"
        :label="t('history.points')"
      ></el-table-column>
    </el-table>
    <el-pagination
      @current-change="handlePageChange"
      :current-page="pagination.pageNo"
      :page-size="pagination.pageSize"
      :total="pagination.total"
      layout="total, prev, pager, next"
      style="width: 100%; margin-top: 20px; text-align: center"
    >
      <template #total>
        {{ t("pagination.total", { total: pagination.total }) }}
      </template>
    </el-pagination>
  </div>
</template>

<script setup lang="ts">
import { ref, watch, computed } from "vue";
import { useI18n } from "vue-i18n";
import { getScoreHistoryList } from "@/api/pmApi.ts";
import { debounce, storageLocal } from "@pureadmin/utils";

const { t } = useI18n();
const tableData = ref([]);
const pagination = ref({
  pageNo: 1,
  pageSize: 10,
  total: 0
});

// 在 computed 部分添加状态转换函数
const getStatusTags = computed(() => {
  return (statusName: string) => {
    if (!statusName) return [];
    return statusName
      .split(",")
      .map(item => item.trim())
      .map(item => {
        const [_, text, number] = item.match(/^([\p{Script=Han}]+)\((\d+)\)$/u);
        return {
          text,
          number
        };
      });
  };
});

const props = defineProps({
  searchInfo: {
    type: Object,
    default: () => ({
      sStatus: "",
      productNo: "",
      productName: ""
    })
  },
  statusList: {
    type: Array,
    default: () => []
  }
});

interface IQueryParams {
  pageNo: number;
  pageSize: number;
  searchStr?: string;
}

const debouncedFetch = debounce(() => {
  fetchProductList();
}, 500);

watch(
  () => props.searchInfo,
  newVal => {
    debouncedFetch();
  },
  { immediate: true, deep: true }
);

const fetchProductList = () => {
  const commonInfo: IQueryParams = {
    pageNo: pagination.value.pageNo,
    pageSize: pagination.value.pageSize
  };
  const searchArr = [] as any;
  Object.keys(props.searchInfo)?.forEach(key => {
    const searchParams = {} as any;
    if (props.searchInfo[key] && props.searchInfo[key] !== "all") {
      searchParams.searchName = key;
      searchParams.searchType = "like";
      searchParams.searchValue = props.searchInfo[key];
      searchArr.push(searchParams);
    }
  });
  searchArr.push({
    searchName: "userId",
    searchType: "equals",
    searchValue: storageLocal().getItem("dataSource")?.id
  });
  commonInfo.searchStr = JSON.stringify(searchArr);
  getScoreHistoryList(commonInfo).then(res => {
    tableData.value = res.data?.records || [];
    pagination.value.total = res.data.total;
  });
};

const handlePageChange = (pageNo: number) => {
  pagination.value.pageNo = pageNo;
  fetchProductList();
};

defineExpose({
  fetchProductList
});
</script>
